import UIKit

extension UIColor {
    class func ESPIDYColorDark() -> UIColor {
        //#383938
        return UIColor(r: 56, g: 57, b: 56)
    }
    
    class func ESPIDYColorLight() -> UIColor {
        //#F0ECED
        return UIColor(r: 240, g: 236, b: 237)
    }
    
    class func ESPIDYColorGreenL() -> UIColor {
        //#47BD96
        return UIColor(r: 71, g: 189, b: 150)
    }
    
    class func ESPIDYColorGreenD() -> UIColor {
        //#3A9473
        return UIColor(r: 58, g: 148, b: 115)
    }
    
    class func ESPIDYColorRedL() -> UIColor {
        //#F16057
        return UIColor(r: 241, g: 96, b: 87)
    }
    
    class func ESPIDYColorRedD() -> UIColor {
        //#BC5050
        return UIColor(r: 188, g: 80, b: 80)
    }

    class func ESPIDYColorUnSelected() -> UIColor {
        //#5F6166
        return UIColor(r: 95, g: 97, b: 102)
    }
    
    class func ESPIDYColorSlideMenu() -> UIColor {
        //#222228
        return UIColor(r: 34, g: 34, b: 40)
    }
    
    class func ESPIDYColorSlideMenuSelect() -> UIColor {
        //#222228
        return UIColor(r: 29, g: 29, b: 35)
    }
    
    class func ESPIDYColorBorderView() -> UIColor {
        //#222228
        return UIColor(r: 211, g: 211, b: 211)
    }
}
